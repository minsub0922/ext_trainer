@dataclass
class PRF1:
    """Precision / recall / F1 bundle with TP/FP/FN counts."""

    precision: float = 0.0
    recall: float = 0.0
    f1: float = 0.0
    tp: int = 0
    fp: int = 0
    fn: int = 0
    support: int = 0  # total golds

    def to_dict(self) -> dict[str, float | int]:
        return {
            "precision": round(self.precision, 4),
            "recall": round(self.recall, 4),
            "f1": round(self.f1, 4),
            "tp": self.tp,
            "fp": self.fp,
            "fn": self.fn,
            "support": self.support,
        }


@dataclass
class IEMetrics:
    """Aggregate IE metrics across all active tasks."""

    kv: PRF1 | None = None
    entity: PRF1 | None = None
    relation: PRF1 | None = None
    relation_typed: PRF1 | None = None  # stricter: also requires matching types
    n_records: int = 0
    n_parse_failures: int = 0
    per_record: list[dict[str, Any]] = field(default_factory=list)

    def to_dict(self, include_per_record: bool = False) -> dict[str, Any]:
        d: dict[str, Any] = {
            "n_records": self.n_records,
            "n_parse_failures": self.n_parse_failures,
        }
        if self.kv is not None:
            d["kv"] = self.kv.to_dict()
        if self.entity is not None:
            d["entity"] = self.entity.to_dict()
        if self.relation is not None:
            d["relation"] = self.relation.to_dict()
        if self.relation_typed is not None:
            d["relation_typed"] = self.relation_typed.to_dict()
        if include_per_record:
            d["per_record"] = self.per_record
        return d


# ---------------------------------------------------------------------------
# Core counting helpers
# ---------------------------------------------------------------------------


def _prf1_from_counts(tp: int, fp: int, fn: int, support: int) -> PRF1:
    precision = tp / (tp + fp) if (tp + fp) else 0.0
    recall = tp / (tp + fn) if (tp + fn) else 0.0
    f1 = (
        2 * precision * recall / (precision + recall)
        if (precision + recall) > 0
        else 0.0
    )
    return PRF1(
        precision=precision,
        recall=recall,
        f1=f1,
        tp=tp,
        fp=fp,
        fn=fn,
        support=support,
    )


def _normalize(text: Any) -> str:
    """Lowercase + collapse whitespace for lenient string matching."""
    if text is None:
        return ""
    return " ".join(str(text).lower().split())


