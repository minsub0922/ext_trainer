# Top-level evaluator
# ---------------------------------------------------------------------------


def evaluate(
    predictions: list[str],
    gold_records: list[dict[str, Any]],
    task_types: list[str] | None = None,
) -> IEMetrics:
    """Compute IE metrics for a batch of predictions against gold records.

    Args:
        predictions: raw model output strings, one per gold record (same order).
        gold_records: canonical records as dicts (``to_canonical_dict()`` output).
        task_types: which tasks to score. Defaults to the union of
            task_types actually present across gold. Values: ``"kv"``,
            ``"entity"``, ``"relation"``.

    Returns:
        An ``IEMetrics`` summary. Only the requested tasks are populated.
    """
    if len(predictions) != len(gold_records):
        raise ValueError(
            f"Length mismatch: {len(predictions)} predictions vs "
            f"{len(gold_records)} gold records."
        )

    n = len(gold_records)
    parsed: list[dict[str, Any]] = []
    parse_failures = 0
    for raw in predictions:
        p, ok = _parse_prediction_with_status(raw or "")
        if raw.strip() and not ok:
            parse_failures += 1
        parsed.append(p)

    # Figure out which tasks to score.
    if task_types is None:
        active: set[str] = set()
        for g in gold_records:
            active.update(g.get("task_types") or [])
        task_types = sorted(active) or ["kv", "entity", "relation"]

    metrics = IEMetrics(n_records=n, n_parse_failures=parse_failures)

    if "kv" in task_types:
        pred_kvs = [p["kv"] for p in parsed]
        gold_kvs = [(g.get("answer") or {}).get("kv", {}) or {} for g in gold_records]
        metrics.kv = score_kv(pred_kvs, gold_kvs)

    if "entity" in task_types:
        pred_ents = [p["entity"] for p in parsed]
        gold_ents = [
            (g.get("answer") or {}).get("entity", []) or [] for g in gold_records
        ]
        metrics.entity = score_entities(pred_ents, gold_ents)

    if "relation" in task_types:
        pred_rels = [p["relation"] for p in parsed]
        gold_rels = [
            (g.get("answer") or {}).get("relation", []) or [] for g in gold_records
        ]
        metrics.relation = score_relations(pred_rels, gold_rels, require_types=False)
        metrics.relation_typed = score_relations(
            pred_rels, gold_rels, require_types=True
        )

    logger.info(
        "Eval summary: n=%d parse_failures=%d "
        "kv_f1=%.4f entity_f1=%.4f relation_f1=%.4f",
        n,
        parse_failures,
        metrics.kv.f1 if metrics.kv else -1,
        metrics.entity.f1 if metrics.entity else -1,
        metrics.relation.f1 if metrics.relation else -1,
    )
    return metrics


