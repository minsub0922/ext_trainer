def score_kv(
    pred_kvs: Iterable[dict[str, Any]],
    gold_kvs: Iterable[dict[str, Any]],
) -> PRF1:
    """Score KV extraction at the (record, field) level.

    A prediction counts as TP iff the gold also has that field AND the
    normalized string value matches. Missing-in-pred = FN; hallucinated
    field (not in gold) = FP.

    Args:
        pred_kvs: sequence of ``{field: value}`` dicts from predictions.
        gold_kvs: sequence of ``{field: value}`` dicts from gold answers.

    Returns:
        PRF1 summary. ``support`` = total gold (record, field) pairs
        with non-empty values.
    """
    tp = fp = fn = support = 0
    for pred, gold in zip(pred_kvs, gold_kvs):
        pred = pred or {}
        gold = gold or {}
        gold_fields = {k: _normalize(v) for k, v in gold.items() if v not in (None, "")}
        pred_fields = {k: _normalize(v) for k, v in pred.items() if v not in (None, "")}

        support += len(gold_fields)

        for field_name, gold_val in gold_fields.items():
            pred_val = pred_fields.get(field_name)
            if pred_val is None:
                fn += 1
            elif pred_val == gold_val:
                tp += 1
            else:
                fn += 1
                fp += 1  # wrong value = both a miss and a hallucination

        # Fields present in pred but not in gold -> pure FP.
        for field_name in pred_fields:
            if field_name not in gold_fields:
                fp += 1

    return _prf1_from_counts(tp, fp, fn, support)


def score_entities(
    pred_entities: Iterable[list[dict[str, Any]]],
    gold_entities: Iterable[list[dict[str, Any]]],
) -> PRF1:
    """Score NER using set-level F1 over (type, normalized_text) tuples.

    Offsets (``start``/``end``) are ignored because IE SFT outputs rarely
    include trustworthy offsets. If the same (type, text) pair occurs
    multiple times in gold, each occurrence is counted once against a
    multiset of predictions (so duplicated predictions don't inflate TP).

    Args:
        pred_entities: per-record list of entity dicts from predictions.
        gold_entities: per-record list of entity dicts from gold.

    Returns:
        PRF1 summary. ``support`` = total gold entities.
    """
    tp = fp = fn = support = 0
    for pred, gold in zip(pred_entities, gold_entities):
        gold_bag: dict[tuple[str, str], int] = {}
        for e in gold or []:
            key = (_normalize(e.get("type")), _normalize(e.get("text")))
            gold_bag[key] = gold_bag.get(key, 0) + 1
        support += sum(gold_bag.values())

        remaining = dict(gold_bag)  # copy; decremented as we match
        for e in pred or []:
            key = (_normalize(e.get("type")), _normalize(e.get("text")))
            if remaining.get(key, 0) > 0:
                remaining[key] -= 1
                tp += 1
            else:
                fp += 1
        fn += sum(remaining.values())

    return _prf1_from_counts(tp, fp, fn, support)


def score_relations(
    pred_rels: Iterable[list[dict[str, Any]]],
    gold_rels: Iterable[list[dict[str, Any]]],
    require_types: bool = False,
) -> PRF1:
    """Score RE using set-level F1 over relation triples.

    By default the tuple key is ``(head, relation, tail)`` (lenient).
    When ``require_types=True`` the key becomes
    ``(head, head_type, relation, tail, tail_type)`` (stricter, closer
    to the "strict" variant used in DocRED/CoNLL04).
    """
    tp = fp = fn = support = 0

    def _key(r: dict[str, Any]) -> tuple:
        if require_types:
            return (
                _normalize(r.get("head")),
                _normalize(r.get("head_type")),
                _normalize(r.get("relation")),
                _normalize(r.get("tail")),
                _normalize(r.get("tail_type")),
            )
        return (
            _normalize(r.get("head")),
            _normalize(r.get("relation")),
            _normalize(r.get("tail")),
        )

    for pred, gold in zip(pred_rels, gold_rels):
        gold_bag: dict[tuple, int] = {}
        for r in gold or []:
            k = _key(r)
            gold_bag[k] = gold_bag.get(k, 0) + 1
        support += sum(gold_bag.values())

        remaining = dict(gold_bag)
        for r in pred or []:
            k = _key(r)
            if remaining.get(k, 0) > 0:
                remaining[k] -= 1
                tp += 1
            else:
                fp += 1
        fn += sum(remaining.values())

    return _prf1_from_counts(tp, fp, fn, support)


